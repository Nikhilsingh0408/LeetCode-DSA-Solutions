/*
Problem Name : Lemonade Change
LeetCode ID  : 860
Difficulty  : Easy
Link        : https://leetcode.com/problems/lemonade-change/

---------------------------------------------------
Approach: Greedy
---------------------------------------------------
Idea:
- Maintain count of $5 and $10 bills.
- Always try to give change optimally.

---------------------------------------------------
Time Complexity:
- O(n)

Space Complexity:
- O(1)

---------------------------------------------------
*/

#include <bits/stdc++.h>
using namespace std;

class Solution {
public:
    bool lemonadeChange(vector<int>& bills) {
        int five = 0, ten = 0;

        for (int bill : bills) {
            if (bill == 5) {
                five++;
            } else if (bill == 10) {
                if (five == 0) return false;
                five--;
                ten++;
            } else { // bill == 20
                if (ten > 0 && five > 0) {
                    ten--;
                    five--;
                } else if (five >= 3) {
                    five -= 3;
                } else {
                    return false;
                }
            }
        }
        return true;
    }
};
